    public boolean validates(){
        boolean success= true;
        int totalQuantity = Integer.parseInt(txtCurrentQuantity.getText()) + 
                Integer.parseInt(txtQuantity.getText());
        
         if(productDAO.getStocks(txtBarcode.getText()) < totalQuantity){
             showMessageError("Insufficient Stocks");
             success = false;
         }
        
        return success;
    }

