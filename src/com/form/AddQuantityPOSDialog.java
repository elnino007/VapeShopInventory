    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(validates()){
            DefaultTableModel model = (DefaultTableModel) posDiaglog.tblPOS.getModel();
            int row = posDiaglog.tblPOS.getSelectedRow();
            int totalQty = Integer.parseInt(txtCurrentQuantity.getText()) + 
                Integer.parseInt(txtQuantity.getText());
            double totalAmount = Double.parseDouble(txtPrice.getText()) * totalQty;
            int totalPoints = totalQty 
                    * Integer.parseInt(model.getValueAt(row, 5).toString());
            
            model.setValueAt(totalQty, row, 2);
            model.setValueAt(totalAmount, row, 4);
            model.setValueAt(totalPoints, row, 5);
            posDiaglog.clearTxtAndButton();
            posDiaglog.transactionInfo();
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

